// Funções Predefinidas em JavaScript

// Entrada e saída de dados:
console.log("Exemplo de saída no console"); // Exibe algo no console
let nome = prompt("Digite seu nome:"); // Pede um dado para o usuárs
// Manipulação de Strings:
let texto = "Exemplo";
console.log(texto.length); // Retorna o tamanho da string
console.log(texto.toUpperCase()); // Transforma em maiúsculas

// Manipulação de Números:
let numero = 4.7;
console.log(Math.round(numero)); // Arredonda um número
console.log(Math.random()); // Gera um número aleatório entre 0 e 1

// Manipulação de Arrays:
let lista = [1, 2, 3];
lista.push(4); // Adiciona um item no final
console.log(lista);
lista.pop(); // Remove o último item
console.log(lista);